public class Q1 {
    // Function to add two numbers
    public static int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 5;
        int sum = add(num1, num2);
        System.out.println(num1 + " + " + num2 + " = " + sum);
    }
}
